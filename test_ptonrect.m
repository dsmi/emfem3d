%% function test_ptonrect

cx = 1.0;
cy = 2.0;
dx = 0.3;
dy = 0.4;

nx = 3;
ny = 4;

p = ptonrect( cx, cy, dx, dy, nx, ny );

pt = [ ...
   0.8500,   1.8000 ; ...
   0.9500,   1.8000 ; ...
   1.0500,   1.8000 ; ...
   1.1500,   1.8000 ; ...
   1.1500,   1.9000 ; ...
   1.1500,   2.0000 ; ...
   1.1500,   2.1000 ; ...
   1.1500,   2.2000 ; ...
   1.0500,   2.2000 ; ...
   0.9500,   2.2000 ; ...
   0.8500,   2.2000 ; ...
   0.8500,   2.1000 ; ...
   0.8500,   2.0000 ; ...
   0.8500,   1.9000 ];

assert( norm( p - pt ) < 1e-10 )
