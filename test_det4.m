function test_det4

A = [ 1, 2, 3, 4; 1, 3, 3, 4; 1, 3, 4, 4; 3, 3, 4, 5 ];

d = det4( A(1,1), A(1,2), A(1,3), A(1,4), ...
          A(2,1), A(2,2), A(2,3), A(2,4), ...
          A(3,1), A(3,2), A(3,3), A(3,4), ...
          A(4,1), A(4,2), A(4,3), A(4,4) );
dt = det( A );

assert( abs( d-dt ) < 1.0e-10 )


